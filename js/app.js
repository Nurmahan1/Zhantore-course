  const typeName = {mcq:'Бір жауап',multi:'Көп жауап',open:'Ашық сұрақ',match:'Сәйкестендіру'}[q.type];
  const typeClass = {mcq:'qt-mcq',multi:'qt-multi',open:'qt-open',match:'qt-match'}[q.type];
