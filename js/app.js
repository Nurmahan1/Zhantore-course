function normalizeAnswer(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[«»“”"'`.,!?;:()\[\]{}]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function checkOpenAnswer(q, value) {
  const val = normalizeAnswer(value);
  if (!val) return false;
  const answers = Array.isArray(q.answers) ? q.answers : [q.answer];
  for (const ansRaw of answers) {
    const ans = normalizeAnswer(ansRaw);
    if (val === ans) return true;
  }
  if (Array.isArray(q.keywords) && q.keywords.length) {
    return q.keywords.every(k => val.includes(normalizeAnswer(k)));
  }
  return false;
}

function shuffledIndexes(n, seedText) {
  const arr = Array.from({length:n}, (_,i)=>i);
  let seed = 0;
  for (const ch of String(seedText)) seed = (seed * 31 + ch.charCodeAt(0)) >>> 0;
  for (let i = arr.length - 1; i > 0; i--) {
    seed = (seed * 1664525 + 1013904223) >>> 0;
    const j = seed % (i + 1);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  if (arr.every((v,i)=>v===i) && arr.length > 1) [arr[0], arr[1]] = [arr[1], arr[0]];
  return arr;
}

function getWrongMsg(q) {
  if (q.type==='mcq') return `Дұрыс жауап: ${q.opts[q.correct]}`;
  if (q.type==='open') return `Дұрыс жауап: ${q.answer}`;
  if (q.type==='multi') return `Дұрыс жауаптар: ${q.correct.map(i=>q.opts[i]).join(', ')}`;
  if (q.type==='match') return `Жұптарды дұрыс сәйкестендіріңіз`;
  return '';
}

