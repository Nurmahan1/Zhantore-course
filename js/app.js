function renderSidebar() {
  const el = document.getElementById('sbList');
  el.innerHTML = '';
  LECTURES.forEach((lec, li) => {
    const done = completedLectures.has(li);
    const locked = li > 0 && !completedLectures.has(li-1);
    const active = currentView.li === li && currentView.type === 'lecture';
    const semOpen = currentView.li === li;

    const row = document.createElement('div');
    row.className = 'lec-row' + (active?' active':'') + (locked?' locked':'');
    row.innerHTML = `
      <div class="lec-icon ${done?'done':active?'current':locked?'locked':''}">${done?'✓':locked?'🔒':lec.num}</div>
      <div class="lec-body">
        <div class="lec-row-title">Лекция ${lec.num}: ${lec.title}</div>
        <div class="lec-row-sub">${lec.time} · ${lec.level}</div>
      </div>
      ${locked?'<span class="lec-lock">🔒</span>':''}
    `;
    row.onclick = locked ? () => toast('🔒 Алдыңғы лекцияны аяқтаңыз!') : () => openLecture(li);
    el.appendChild(row);

    // Seminar rows
    const semBox = document.createElement('div');
    semBox.className = 'sem-rows' + (semOpen?' open':'');
    lec.seminars.forEach((sem, si) => {
      const semKey = li+'-'+si;
      const semDone = completedSeminars.has(semKey);
      const semActive = currentView.li===li && currentView.si===si && currentView.type==='seminar';
      const semRow = document.createElement('div');
      semRow.className = 'sem-row' + (semActive?' active':'');
      semRow.innerHTML = `
        <div class="sem-dot ${semDone?'done':semActive?'active':''}"></div>
        <span class="sem-row-title">${sem.icon} ${sem.title}</span>
        <span class="sem-tag">${semDone?'✅':'Семинар'}</span>
      `;
      semRow.onclick = e => { e.stopPropagation(); openSeminar(li, si); };
      semBox.appendChild(semRow);
    });
    el.appendChild(semBox);
  });

  // Header progress
  const pct = Math.round(completedLectures.size/15*100);
  document.getElementById('hpf').style.width = pct+'%';
  document.getElementById('hpt').textContent = completedLectures.size+'/15';
  document.getElementById('rf').style.strokeDashoffset = 107-(107*pct/100);
  document.getElementById('rn').textContent = pct+'%';
  document.getElementById('pl').textContent = completedLectures.size+' лекция аяқталды';
  document.getElementById('hsc').textContent = '⭐ '+score+' ұпай';
}

