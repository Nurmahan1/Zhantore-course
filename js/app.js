function initHints(semKey) {
  if (!aiHintLevel[semKey]) aiHintLevel[semKey] = 0;
}

function showHint(semKey) {
  initHints(semKey);
  const li = parseInt(semKey.split('-')[0]);
  const si = parseInt(semKey.split('-')[1]);
  const prob = LECTURES[li].seminars[si].problem;
  const level = aiHintLevel[semKey];

  const hints = generateHints(prob, li, si);
  const hintBox = document.getElementById('hintBox-' + semKey);
  if (!hintBox) return;

  hintBox.classList.remove('hidden');
  const levelBar = Array.from({length:3}, (_,i) =>
    `<div class="hint-level-dot ${i<=level?'active':''}"></div>`).join('');

  const levelNames = ['💡 Намек', '🔍 Жартылай шешім', '📖 Толық түсіндірме'];
  hintBox.innerHTML = `
    <div class="hint-level-bar">${levelBar}</div>
    <strong>${levelNames[level]}</strong><br><br>
    ${hints[level]}
    ${level < 2 ? `<br><br><button class="hint-btn" onclick="nextHint('${semKey}')">Келесі кеңес ›</button>` : ''}
  `;
  aiHintLevel[semKey] = Math.min(level, 2);
}

function nextHint(semKey) {
  aiHintLevel[semKey] = Math.min((aiHintLevel[semKey] || 0) + 1, 2);
  showHint(semKey);
}

function generateHints(prob, li, si) {
  const title = prob.title;
  const desc = prob.desc.slice(0, 100);
  return [
    `🤔 Есепті мұқият оқы: "${desc}..."

Алгоритмді ойла:
• Қандай дерек енгізіледі?
• Қандай есептеу керек?
• Нәтижені қалай шығару керек?`,
    `📝 Жалпы бағыт:
${prob.constraints.replace(/\n/g, '\n')}

Кодты жазып, өз input мәндеріңмен іске қосып көр.`,
    `✅ Кеңес:
${prob.starterCode}
# Осы бастапқы кодтан бастап, есептің шартын орында.
# Run батырмасымен нәтижені тексеріп көр.`
  ];
}

// Add hint button via direct DOM after openSeminar renders
// We hook into the existing openSeminar by appending logic at the bottom of the original function.
// Instead of patching, we use a MutationObserver on tab-lesson to inject hint UI.
(function setupHintInjector() {
  const observer = new MutationObserver(() => {
    const runRow = document.querySelector('.run-row');
    if (runRow && !runRow.querySelector('.hint-btn') && currentView.type === 'seminar') {
      const semKey = currentView.li + '-' + currentView.si;
      const hintBtn = document.createElement('button');
      hintBtn.className = 'hint-btn';
      hintBtn.textContent = '💡 Кеңес';
      hintBtn.onclick = () => showHint(semKey);
      runRow.appendChild(hintBtn);
      const hintBox = document.createElement('div');
      hintBox.id = 'hintBox-' + semKey;
      hintBox.className = 'hint-box hidden';
      runRow.parentNode.insertBefore(hintBox, runRow.nextSibling);
    }
  });
  observer.observe(document.getElementById('tab-lesson'), {childList: true, subtree: false});
})();

