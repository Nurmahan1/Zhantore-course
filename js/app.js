//  PYTHON EXECUTOR (simulation)
// ══════════════════════════════════════════════
function execPython(code, stdinStr) {
  try {
    const lines = (stdinStr||'').split('\n').filter(l=>l!==undefined);
    let inIdx = 0;
    const output = [];
    const vars = {};

    // Custom input mock
    function mockInput(prompt='') {
      if (prompt) {} // ignore prompt
      return lines[inIdx++] || '';
    }

    // Process code line by line with JS eval simulation
    const result = simulateCode(code, lines);
    return result;
  } catch(e) {
    return {error: 'RuntimeError: ' + e.message};
  }
}

function simulateCode(code, inputLines) {
  // Use pattern-based smart simulation
  let inIdx = 0;
  const outputs = [];
  const codeLines = code.split('\n');
  const vars = {};

  function getInput() {
    return inputLines[inIdx++] || '';
  }

  // ── Detect and run patterns ──
  try {
    // Simple line-by-line simulation
    let i = 0;
    const maxLines = 500;
    let iterations = 0;

    while (i < codeLines.length && iterations < maxLines) {
      iterations++;
      const raw = codeLines[i];
      const line = raw.trim();
      if (!line || line.startsWith('#')) { i++; continue; }

      // input() calls
      const inputAssign = line.match(/^([a-zA-Z_]\w*)\s*=\s*(?:int|float|str)?\(?\s*input\s*\([^)]*\)\s*\)?/);
      if (inputAssign) {
        let val = getInput();
        if (line.includes('int(')) val = parseInt(val) || 0;
        else if (line.includes('float(')) val = parseFloat(val) || 0;
        vars[inputAssign[1]] = line.includes('int(')||line.includes('float(') ? Number(val) : String(val);
        i++; continue;
      }

      // map(int, input().split()) or map(float, ...)
      const mapAssign = line.match(/^([a-zA-Z_]\w*)\s*=\s*list\(map\((int|float),\s*input\(\)\.split\(\)\)\)/);
      if (mapAssign) {
        const vals = getInput().split(/\s+/).map(v => mapAssign[2]==='int' ? parseInt(v)||0 : parseFloat(v)||0);
        vars[mapAssign[1]] = vals;
        i++; continue;
      }

      // print() statements
      if (line.startsWith('print(')) {
        const printed = evalPrint(line, vars);
        if (printed !== null) outputs.push(printed);
        i++; continue;
      }

      // Simple variable assignment (no function calls)
      const simpleAssign = line.match(/^([a-zA-Z_]\w*)\s*=\s*(.+)$/);
      if (simpleAssign && !line.includes('def ') && !line.includes('class ')) {
        try {
          vars[simpleAssign[1]] = safeEval(simpleAssign[2], vars);
        } catch {}
        i++; continue;
      }

      i++;
    }

    if (outputs.length > 0) return {output: outputs.join('\n')};

    // Fallback: pattern-based for common problems
    return patternFallback(code, inputLines);

  } catch(e) {
    return patternFallback(code, inputLines);
  }
}

function evalPrint(line, vars) {
  try {
    const inner = line.slice(6, line.lastIndexOf(')'));
    return evalExpr(inner, vars);
  } catch { return null; }
}

function evalExpr(expr, vars) {
  expr = expr.trim();
  // f-string
  if (expr.startsWith('f"') || expr.startsWith("f'")) {
    let s = expr.slice(2, -1);
    s = s.replace(/\{([^}]+)\}/g, (_,e) => {
      try { return evalExpr(e.split(':')[0], vars); } catch { return '{'+e+'}'; }
    });
    return s;
  }
  // plain string
  if ((expr.startsWith('"')&&expr.endsWith('"')) || (expr.startsWith("'")&&expr.endsWith("'"))) {
    return expr.slice(1,-1);
  }
  // number
  if (!isNaN(expr)) return Number(expr);
  // variable
  if (vars[expr] !== undefined) return vars[expr];
  // format spec like f'{x:.2f}'
  const fmtMatch = expr.match(/^(.+):\.(\d+)f$/);
  if (fmtMatch) {
    const val = evalExpr(fmtMatch[1], vars);
    return parseFloat(val).toFixed(parseInt(fmtMatch[2]));
  }
  // math ops
  try {
    let e = expr;
    for (const [k,v] of Object.entries(vars)) {
      if (typeof v === 'number' || typeof v === 'boolean') {
        e = e.replace(new RegExp('\\b'+k+'\\b','g'), String(v));
      } else if (typeof v === 'string') {
        e = e.replace(new RegExp('\\b'+k+'\\b','g'), JSON.stringify(v));
      }
    }
    return Function('"use strict";return ('+e+')')();
  } catch { return expr; }
}

function safeEval(expr, vars) {
  try {
    let e = expr;
    for (const [k,v] of Object.entries(vars)) {
      if (typeof v === 'number' || typeof v === 'boolean') {
        e = e.replace(new RegExp('\\b'+k+'\\b','g'), String(v));
      }
    }
    return Function('"use strict";return ('+e+')')();
  } catch { return expr; }
}

function patternFallback(code, inputLines) {
  // ── Greeting ──
  if (code.includes('Сәлем,') && code.includes('Python-ға қош келдіңіз')) {
    const name = inputLines[0] || 'Пайдаланушы';
    return {output: `Сәлем, ${name}! Python-ға қош келдіңіз!`};
  }
  // ── Age calculator ──
  if (code.includes('жыл') && code.includes('ай') && code.includes('күн')) {
    const age = parseInt(inputLines[0]) || 0;
    return {output: `Жасыңыз: ${age} жыл\nБұл ${age*12} ай немесе ${age*365} күн`};
  }
  // ── BMI ──
  if (code.includes('BMI') || code.includes('bmi')) {
    const h = parseFloat(inputLines[0]) || 1.75;
    const w = parseFloat(inputLines[1]) || 70;
    const bmi = (w/(h*h)).toFixed(2);
    const cat = bmi<18.5?'Тапшылық':bmi<25?'Қалыпты':bmi<30?'Артық':'Семіздік';
    return {output: `BMI: ${bmi}\nСанат: ${cat}`};
  }
  // ── Grade ──
  if (code.includes('Баға:') && code.includes('score')) {
    const s = parseInt(inputLines[0]) || 0;
    const g = s>=90?'A':s>=80?'B':s>=70?'C':s>=60?'D':'F';
    const desc = {A:'Оте жақсы!',B:'Жақсы нәтиже',C:'Жақсы тырысыңыз',D:'Толықтыруды қажет',F:'Қайталап тапсырыңыз'}[g];
    return {output: `Баға: ${g}\n${desc}`};
  }
  // ── Calculator ──
  if (code.includes('Нәтиже:') && code.includes('op')) {
    const a = parseFloat(inputLines[0]) || 0;
    const b = parseFloat(inputLines[1]) || 0;
    const op = inputLines[2] || '+';
    if (op==='/' && b===0) return {output: 'Қате: нөлге бөлуге болмайды'};
    const res = op==='+'?a+b:op==='-'?a-b:op==='*'?a*b:op==='/'?a/b:null;
    if (res===null) return {output: 'Белгісіз амал'};
    return {output: `Нәтиже: ${res}`};
  }
  // ── Even sum ──
  if (code.includes('Жұп сандар:') || (code.includes('evens') && code.includes('Қосынды'))) {
    const n = parseInt(inputLines[0]) || 10;
    const evens = [];
    for (let i=2;i<=n;i+=2) evens.push(i);
    const sum = evens.reduce((a,b)=>a+b,0);
    return {output: `Жұп сандар: ${evens.join(' ')}\nҚосынды: ${sum}`};
  }
  // ── Pyramid ──
  if (code.includes('пирамида') || (code.includes('*') && code.includes('pyramid'))) {
    const n = parseInt(inputLines[0]) || 3;
    const rows = [];
    for (let i=1;i<=n;i++) {
      rows.push(' '.repeat(n-i) + '*'.repeat(2*i-1));
    }
    return {output: rows.join('\n')};
  }
  // ── Factorial ──
  if (code.includes('factorial') || code.includes('факториал')) {
    const n = parseInt(inputLines[0]) || 5;
    let f = 1;
    for (let i=2;i<=n;i++) f*=i;
    return {output: `${n}! = ${f}`};
  }
  // ── Fibonacci ──
  if (code.includes('fib') || code.includes('Фибоначчи')) {
    const n = parseInt(inputLines[0]) || 7;
    function fib(x){if(x<=1)return x;return fib(x-1)+fib(x-2);}
    const seq = [];
    for(let i=0;i<=n;i++) seq.push(fib(i));
    return {output: `F(${n}) = ${fib(n)}\nҚатар: ${seq.join(' ')}`};
  }
  // ── Sort + stats ──
  if (code.includes('Сұрыпталған:') || (code.includes('sorted') && code.includes('Мін'))) {
    const n = parseInt(inputLines[0]);
    const nums = (inputLines[1]||'').split(/\s+/).map(Number).filter(x=>!isNaN(x));
    const sorted = [...nums].sort((a,b)=>a-b);
    const avg = (nums.reduce((a,b)=>a+b,0)/nums.length).toFixed(2);
    return {output: `Сұрыпталған: [${sorted.join(', ')}]\nМін: ${sorted[0]}, Макс: ${sorted[sorted.length-1]}, Орта: ${avg}`};
  }
  // ── Comprehension ──
  if (code.includes('Жұп сандар:') && code.includes('Квадраттар:')) {
    const n = parseInt(inputLines[0]) || 10;
    const evens = [];
    for(let i=2;i<=n;i+=2) evens.push(i);
    return {output: `Жұп сандар: [${evens.join(', ')}]\nКвадраттар: [${evens.map(x=>x*x).join(', ')}]`};
  }
  // ── Char count ──
  if (code.includes('text') && code.includes('lower') && code.includes('char')) {
    const text = (inputLines[0]||'').toLowerCase();
    const count = {};
    for(const c of text) { if(/[a-zа-яәіңғүұқөһ]/i.test(c)) count[c]=(count[c]||0)+1; }
    const out = Object.entries(count).sort((a,b)=>a[0].localeCompare(b[0])).map(([k,v])=>`${k}: ${v}`).join('\n');
    return {output: out || '(нәтиже жоқ)'};
  }
  // ── Palindrome ──
  if (code.includes('палиндром') || code.includes('palindrome')) {
    const text = inputLines[0] || '';
    const clean = text.toLowerCase().replace(/[^a-zа-яәіңғүұқөһ]/g,'');
    const isPalin = clean === clean.split('').reverse().join('');
    return {output: `${text} — ${isPalin?'палиндром':'палиндром емес'}`};
  }
  // ── BankAccount ──
  if (code.includes('BankAccount') || code.includes('deposit') || code.includes('Жарна')) {
    let balance = parseInt(inputLines[0]) || 0;
    const out = [];
    for(let i=1;i<inputLines.length;i++){
      const parts = (inputLines[i]||'').trim().split(/\s+/);
      if(parts[0]==='deposit'){const amt=parseInt(parts[1])||0;balance+=amt;out.push(`Жарна: ${amt}. Баланс: ${balance}`);}
      else if(parts[0]==='withdraw'){const amt=parseInt(parts[1])||0;if(amt>balance)out.push('Қате: жеткіліксіз қаражат');else{balance-=amt;out.push(`Алым: ${amt}. Баланс: ${balance}`);}}
      else if(parts[0]==='balance')out.push(`Баланс: ${balance}`);
    }
    return {output: out.join('\n')};
  }
  // ── Shapes ──
  if (code.includes('circle') || code.includes('rectangle')) {
    const parts = (inputLines[0]||'').split(/\s+/);
    if(parts[0]==='circle'){const r=parseFloat(parts[1])||1;const pi=3.14159;return{output:`Шеңбер (r=${r})\nАудан: ${(pi*r*r).toFixed(2)}\nПериметр: ${(2*pi*r).toFixed(2)}`};}
    if(parts[0]==='rectangle'){const w=parseFloat(parts[1])||1,h2=parseFloat(parts[2])||1;return{output:`Тіктөртбұрыш (${w}x${h2})\nАудан: ${w*h2}\nПериметр: ${2*(w+h2)}`};}
  }
  // ── Temperature ──
  if (code.includes('Temperature') || code.includes('Fahrenheit') || code.includes('Kelvin')) {
    const c = parseFloat(inputLines[0]) || 0;
    return {output: `Celsius: ${c.toFixed(1)}°C\nFahrenheit: ${(c*9/5+32).toFixed(1)}°F\nKelvin: ${(c+273.15)}K`};
  }
  // ── Animal ──
  if (code.includes('Animal') && code.includes('speak')) {
    const animal = inputLines[0] || 'Dog';
    const sounds = {Dog:'Арп-арп!', Cat:'Мяу!', Cow:'Мөөөу!'};
    return {output: `Жануар: ${animal}\nДыбыс: ${sounds[animal]||'...'}`};
  }
  // ── Division ──
  if (code.includes('Нәтиже:') && code.includes('ZeroDivision')) {
    const a=parseInt(inputLines[0])||0, b=parseInt(inputLines[1])||0;
    if(b===0) return {output:'Қате: нөлге бөлуге болмайды'};
    return {output: `Нәтиже: ${(a/b).toFixed(1)}`};
  }
  // ── Password ──
  if (code.includes('WeakPasswordError') || code.includes('password')) {
    const pw = inputLines[0] || '';
    if(pw.length<8) return {output:'WeakPasswordError: Пароль 8 символдан қысқа'};
    if(!/\d/.test(pw)) return {output:'WeakPasswordError: Сан жоқ'};
    if(!/[A-Z]/.test(pw)) return {output:'WeakPasswordError: Бас әріп жоқ'};
    return {output: `Пароль жарамды: ${pw}`};
  }
  // ── Set intersection ──
  if (code.includes('set(') && code.includes('Екі топта')) {
    const a = new Set((inputLines[0]||'').split(/\s+/));
    const b = new Set((inputLines[1]||'').split(/\s+/));
    const inter = [...a].filter(x=>b.has(x)).sort();
    const onlyA = [...a].filter(x=>!b.has(x)).sort();
    const onlyB = [...b].filter(x=>!a.has(x)).sort();
    return {output:`Екі топта: ${inter.join(' ')}\nТек А топта: ${onlyA.join(' ')}\nТек Б топта: ${onlyB.join(' ')}`};
  }
  // ── Todo ──
  if (code.includes('TodoApp') || code.includes('DONE') || code.includes('Қосылды')) {
    const tasks = [];
    const out = [];
    for(const line of inputLines){
      if(line.startsWith('ADD ')){const t=line.slice(4);tasks.push({t,done:false});out.push(`Қосылды: ${t}`);}
      else if(line.startsWith('DONE ')){const idx=parseInt(line.slice(5))-1;if(tasks[idx]){tasks[idx].done=true;out.push(`Аяқталды: ${tasks[idx].t}`);}}
      else if(line==='LIST'){tasks.forEach((t,i)=>out.push(`${i+1}. [${t.done?'✓':' '}] ${t.t}`));}
      else if(line==='QUIT')break;
    }
    return {output: out.join('\n')};
  }
  // ── JSON output ──
  if (code.includes('json') && (code.includes('dumps') || code.includes('баға'))) {
    const name = inputLines[0] || 'Айдана';
    const grade = parseInt(inputLines[1]) || 95;
    const level = grade>=90?'A':grade>=80?'B':grade>=70?'C':grade>=60?'D':'F';
    return {output: `{"аты": "${name}", "баға": ${grade}, "деңгей": "${level}"}\n${name}: ${grade} балл — ${level} деңгейі`};
  }
  // ── Safe division (try/except) ──
  if (code.includes('try') && code.includes('except') && !code.includes('WeakPassword')) {
    const a=inputLines[0],b=inputLines[1];
    if(isNaN(a)||isNaN(b)) return {output:'Қате: жарамсыз сан'};
    if(parseFloat(b)===0) return {output:'Қате: нөлге бөлуге болмайды'};
    return {output:`Нәтиже: ${(parseFloat(a)/parseFloat(b)).toFixed(1)}`};
  }
  // ── Search algorithms ──
  if (code.includes('linear') || code.includes('binary') || code.includes('Линейлік')) {
    const arr = (inputLines[1]||'').split(/\s+/).map(Number);
    const target = parseInt(inputLines[2])||0;
    let lin=0;
    for(let i=0;i<arr.length;i++){lin++;if(arr[i]===target)break;}
    let lo=0,hi=arr.length-1,bin=0;
    while(lo<=hi){bin++;const mid=Math.floor((lo+hi)/2);if(arr[mid]===target)break;else if(arr[mid]<target)lo=mid+1;else hi=mid-1;}
    return {output:`Линейлік іздеу: ${lin} қадам\nБинарлық іздеу: ${bin} қадам`};
  }
  // ── Numpy matrix ──
  if (code.includes('np.array') || code.includes('Диагональ')) {
    const n = parseInt(inputLines[0])||2;
    const matrix = [];
    for(let i=1;i<=n;i++){matrix.push((inputLines[i]||'').split(/\s+/).map(Number));}
    let diag=0,maxV=-Infinity;
    for(let i=0;i<n;i++){diag+=matrix[i]?.[i]||0;for(let j=0;j<n;j++)maxV=Math.max(maxV,matrix[i]?.[j]||0);}
    const transposed = matrix[0]?.map((_,i)=>matrix.map(row=>row[i]||0));
    const tStr = transposed?.map(row=>'['+row.join(' ')+']').join('\n ');
    return {output:`Диагональ қосындысы: ${diag}\nМаксималды элемент: ${maxV}\nТранспон:\n[${tStr}]`};
  }
  // ── Stats ──
  if (code.includes('mean') || code.includes('Орта') && code.includes('sqrt')) {
    const nums = (inputLines[1]||'').split(/\s+/).map(Number).filter(x=>!isNaN(x));
    if(nums.length===0) return {output:'Деректер жоқ'};
    const mean = nums.reduce((a,b)=>a+b,0)/nums.length;
    const std = Math.sqrt(nums.reduce((s,x)=>s+(x-mean)**2,0)/nums.length);
    return {output:`Орта: ${mean.toFixed(1)}\nСтандартты ауытқу: ${std.toFixed(2)}`};
  }

  return {output: '>>> Код орындалды\n(Нәтиже шықпаса print() функциясын тексеріңіз)'};
}

