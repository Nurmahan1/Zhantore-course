function submitQuiz(key) {
  const parts = key.split('-');
  const li = parseInt(parts[0]), si = parseInt(parts[1]), qi = parseInt(parts[2]);
  const semKey = li+'-'+si;
  const q = LECTURES[li].seminars[si].quizzes[qi];
  let correct = false;

  if (q.type === 'mcq') {
    const sel = quizState[key]?.selected;
    if (sel === undefined) { toast('Жауапты таңдаңыз!'); return; }
    correct = sel === q.correct;
    quizState[key] = {answered:correct, attempted:true, selected:sel, correct};

  } else if (q.type === 'multi') {
    const sel = multiState[key]?.sel || new Set();
    const correctSet = new Set(q.correct);
    correct = sel.size===correctSet.size && [...sel].every(x=>correctSet.has(x));
    quizState[key] = {answered:correct, attempted:true, selected:[...sel], correct};
    if (!correct) multiState[key] = {sel:new Set(sel)};

  } else if (q.type === 'open') {
    const rawVal = document.getElementById('open-'+key)?.value?.trim() || '';
    correct = checkOpenAnswer(q, rawVal);
    quizState[key] = {answered:correct, attempted:true, inputVal:rawVal, correct};

  } else if (q.type === 'match') {
    const ms = matchState[key];
    if (!ms || Object.keys(ms.pairs).length < q.pairs.length) { toast('Барлық жұптарды сәйкестендіріңіз!'); return; }
    correct = q.pairs.every((_,i) => ms.pairs[i] === i);
    quizState[key] = {answered:correct, attempted:true, correct};
  }

  if (correct) {
    if (!quizState[key].pointsAdded) { score += q.pts; quizState[key].pointsAdded = true; }
    toast(`✅ Дұрыс! +${q.pts} ұпай`, 'green');
  } else { toast('❌ Қате жауап. Қайта тексеріңіз.', 'red'); }

  document.getElementById('hsc').textContent = '⭐ '+score+' ұпай';
  saveProgress();
  // Re-render this quiz
  document.getElementById('qb-'+key).outerHTML = renderQuiz(q, semKey, qi);
  // Check if all done
  checkSemComplete(li, si);
}

